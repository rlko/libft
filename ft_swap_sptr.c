/*
** CAN'T EXPLAIN THIS SHIT BUT IT REMOVES THE TMP VAR
** s1 = *s1 + (*s1 - *s2);
** s2 = *s1 - (*s1 - *s2)/2;
** s1 = *s1 - (*s1 - *s2)*2;
*/

void	ft_swap_sptr(char **s1, char **s2)
{
	char *tmp;

	tmp = *s1;
	*s1 = *s2;
	*s2 = tmp;
}
